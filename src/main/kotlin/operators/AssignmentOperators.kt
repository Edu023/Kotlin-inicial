package operators

fun main () {

    var x = 5
    println(x)

    x += 2 // x = x + 2
    println(x)

    x *= 2 // x = x * 2
    println(x)

    x -= 2 // x = x - 2
    println(x)

    x /= 2 // x = x / 2
    println(x)

    x++ //para add mais um
    println(x)

    x-- //para diminuir um
    println(x)

}